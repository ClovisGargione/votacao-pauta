package com.votacao.domain.associado;

public class AssociadoException extends Exception {

	private static final long serialVersionUID = 1L;

	public AssociadoException() {
		super();
	}

	public AssociadoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AssociadoException(String message, Throwable cause) {
		super(message, cause);
	}

	public AssociadoException(String message) {
		super(message);
	}

	public AssociadoException(Throwable cause) {
		super(cause);
	}
	
	

}
